package testeito;



public class WhileForTest {

    public static void main(String[] args) {
                
        for (int x = 0; x <= 10; x++) {
            System.out.print(x + "   ");
        }
        
        int x = 0;
        
        while(true){
            x++;
            System.out.println(x);
        }
    }
}
